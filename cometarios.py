# COMENTÁRIOS NO CÓDIGO PYTHON
# Em Python, existem principalmente duas formas de adicionar comentários ao seu código:
#
# Comentários de uma única linha:
# Utiliza-se o símbolo # para adicionar comentários de uma única linha.
# Tudo após o # na mesma linha será considerado um comentário e não será executado.
# Comentários de múltiplas linhas (Docstrings):
# Embora o Python não tenha uma maneira nativa de fazer comentários de múltiplas linhas,
# como em algumas outras linguagens de programação, é comum usar docstrings (strings de documentação) para esse propósito.
# Docstrings são geralmente usados para documentar funções, classes e módulos,
# mas também podem ser usados como comentários de múltiplas linhas. Eles são delimitados por aspas triplas (''' ou """).




print('o resultado de 2 + 3 é:', 2 + 3)
print(2+3*3)
print(4**2/3)
print((9**2)*6-1)
